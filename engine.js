// Decorator functions
engine.numParse = function(n) {
	if (n <= 32767) return {type: 'raw', value: n, register: false, str:engine.hexDecorate(n)};

	var register = n-32768;
	if (register > 7) return false;
	return {type: 'register', value: engine.registers[register], register: register, str:'R'+register+' ('+engine.hexDecorate(engine.registers[register])+')'};
}
engine.binStr = function(n, length) {
	var bin = n.toString(2),
	    length = length || 16;
	while (bin.length < length) bin = '0'+bin;
	return bin.match(/.{4}/g).join(' ');
}
engine.hexDecorate = function(i) {
	return '<span class="hex" title="'+engine.intCommas(i)+'">0x'+i.toString(16)+'</span>';
}
engine.intCommas = function(i) {
	var i = parseInt(i)+'',
	    formatted = '';
	while (i.length > 3) {
		formatted = ','+i.substr(-3)+formatted;
		i = i.substr(0, i.length-3);
	}
	return i+formatted;
}

// Main functions
engine.drawMemory = function() {
	var start = +new Date();
	// Show address space
	var html = '';
	var $memory = $('#memory').empty();
	$.each(engine.memory, function (i, v) {
		var hex = v.toString(16);
		while (hex.length < 4) {
			hex = '0'+hex;
		}
		if (i % 32 == 0) {
			if (i > 0) { html += '<br />'; }
			html += '<span class="rowhead" title="'+i+'">0x'+i.toString(16)+":</span>";
		}
		html += '<span id="a'+i+'" class="hex';
		if (i == engine.cursor) { html += ' active'; }
		if (jQuery.inArray(i, engine.breakpoints) != -1) { html += ' breakpoint'; }
		html +='" title="Address: 0x'+i.toString(16)+' ('+i+'), Decimal: '+v+'">'+hex+'</span>';
	});
	console.log('constructed html in '+(new Date()-start)+' microseconds');
	$('#memory').html(html);
	console.log('inserted html in '+(new Date()-start)+' microseconds');
	
	engine.drawRegisters(); // Show registers
	engine.drawStack(); // Show stack
}
