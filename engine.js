// Update cursor location.
// Returns the old position, so it can be used like `engine.memory[engine.cursor++]`
// (get current value, and increment for the next call) as `engine.memory[engine.setCursor()]`
engine.setCursor = function(n) {
	var n = n || engine.cursor+1,
	    $memory = $('#memory'),
	    old = engine.cursor;
	$memory.find('#a'+engine.cursor).removeClass('active');
	engine.cursor = n;
	var $current = $memory.find('#a'+n).addClass('active');
	//$memory.stop(true).animate({scrollTop: $current.offset().top-100}, 'fast');
	return old;
}
engine.setMem = function(address, value) {
	engine.memory[address] = value;
	var hex = value.toString(16);
	while (hex.length < 4) {
		hex = '0'+hex;
	}
	$('#memory #a'+address).html(hex).attr('title', 'Address: 0x'+address.toString(16)+' ('+address+'), Decimal: '+value).addClass('dirty');
}

