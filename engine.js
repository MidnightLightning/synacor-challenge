engine.do = function(logging) { // Step through one program iteration
	var start = engine.cursor,
	    cmd = engine.memory[engine.setCursor()],
	    log = '<span class="cursor-position">0x'+start.toString(16)+'</span>: ',
	    success = true;
	if (typeof logging == 'undefined') logging = true;
	switch(cmd) {
		case 1:
			log += '<span class="command">SET</span>';
			var a = engine.numParse(engine.memory[engine.setCursor()]),
			    b = engine.numParse(engine.memory[engine.setCursor()]);
			if (a.type = 'register') {
				engine.registers[a.register] = b.value;
				engine.drawRegisters();
			}
			log += ' '+a.str+' = '+b.str;
			break;
		case 2:
			log += '<span class="command">PUSH</span>';
			var a = engine.numParse(engine.memory[engine.setCursor()]);
			engine.stack.push(a.value);
			engine.drawStack();
			log += ' '+a.str;
			break;
		case 3:
			log += '<span class="command">POP</span>';
			var a = engine.numParse(engine.memory[engine.setCursor()]);
			if (a.type == 'register') {
				engine.registers[a.register] = engine.stack.pop();
				engine.drawRegisters();
				engine.drawStack();
			}
			log += ' '+a.str+' = '+engine.hexDecorate(engine.registers[a.register]);
			break;
		case 4:
			log += '<span class="command">EQ</span>';
			var a = engine.numParse(engine.memory[engine.setCursor()]),
			    b = engine.numParse(engine.memory[engine.setCursor()]),
			    c = engine.numParse(engine.memory[engine.setCursor()]);
			if (a.type == 'register') {
				engine.registers[a.register] = (b.value == c.value)? 1 : 0;
				engine.drawRegisters();
			}
			log += ' '+a.str+' = ('+b.str+' == '+c.str+')? 1 : 0 = '+engine.registers[a.register];
			break;
		case 5:
			log += '<span class="command">GT</span>';
			var a = engine.numParse(engine.memory[engine.setCursor()]),
			    b = engine.numParse(engine.memory[engine.setCursor()]),
			    c = engine.numParse(engine.memory[engine.setCursor()]);
			if (a.type == 'register') {
				engine.registers[a.register] = (b.value > c.value)? 1 : 0;
				engine.drawRegisters();
			}
			log += ' '+a.str+' = ('+b.str+' > '+c.str+')? 1 : 0 = '+engine.registers[a.register];
			break;
		case 6:
			log += '<span class="command">JMP</span>';
			var address = engine.numParse(engine.memory[engine.cursor]);
			engine.setCursor(address.value);
			log += ' '+address.str;
			break;
		case 7:
			log += '<span class="command">JT</span>';
			var a = engine.numParse(engine.memory[engine.setCursor()]),
			    b = engine.numParse(engine.memory[engine.setCursor()]);
			if (a.value != 0) {
				log += ' '+a.str+' is not zero, so jumping to '+b.str;
				engine.setCursor(b.value);
			} else {
				log += ' '+a.str+' is zero, so not jumping to '+b.str;
			}
			break;
		case 8:
			log += '<span class="command">JF</span>';
			var a = engine.numParse(engine.memory[engine.setCursor()]),
			    b = engine.numParse(engine.memory[engine.setCursor()]);
			if (a.value == 0) {
				log += ' '+a.str+' is zero, so jumping to '+b.str;
				engine.setCursor(b.value);
			} else {
				log += ' '+a.str+' is not zero, so not jumping to '+b.str;
			}
			break;
		case 9:
			log += '<span class="command">SUM</span>';
			var a = engine.numParse(engine.memory[engine.setCursor()]),
			    b = engine.numParse(engine.memory[engine.setCursor()]),
			    c = engine.numParse(engine.memory[engine.setCursor()]);
			if (a.type == 'register') {
				engine.registers[a.register] = (b.value + c.value) % 32768;
				engine.drawRegisters();
			}
			log += ' '+a.str+' = ('+b.str+' + '+c.str+') % 32768 = '+engine.hexDecorate(engine.registers[a.register]);
			break;
		case 10:
			log += '<span class="command">MULT</span>';
			var a = engine.numParse(engine.memory[engine.setCursor()]),
			    b = engine.numParse(engine.memory[engine.setCursor()]),
			    c = engine.numParse(engine.memory[engine.setCursor()]);
			if (a.type == 'register') {
				engine.registers[a.register] = (b.value * c.value) % 32768;
				engine.drawRegisters();
			}
			log += ' '+a.str+' = ('+b.str+' * '+c.str+') % 32768 = '+engine.hexDecorate(engine.registers[a.register]);
			break;
		case 11:
			log += '<span class="command">MOD</span>';
			var a = engine.numParse(engine.memory[engine.setCursor()]),
			    b = engine.numParse(engine.memory[engine.setCursor()]),
			    c = engine.numParse(engine.memory[engine.setCursor()]);
			if (a.type == 'register') {
				engine.registers[a.register] = b.value % c.value;
				engine.drawRegisters();
			}
			log += ' '+a.str+' = '+b.str+' % '+c.str+' = '+engine.hexDecorate(engine.registers[a.register]);
			break;
		case 12:
			log += '<span class="command">AND</span>';
			var a = engine.numParse(engine.memory[engine.setCursor()]),
			    b = engine.numParse(engine.memory[engine.setCursor()]),
			    c = engine.numParse(engine.memory[engine.setCursor()]);
			if (a.type == 'register') {
				engine.registers[a.register] = (b.value & c.value);
				engine.drawRegisters();
			}
			log += ' '+a.str+' = '+b.str+' & '+c.str+' = '+engine.hexDecorate(engine.registers[a.register])+'<br /><span class="binary">'+engine.binStr(b.value)+'</span><br /><span class="binary" style="border-bottom:solid 1px #CCC;">'+engine.binStr(c.value)+'</span><br /><span class="binary">'+engine.binStr(engine.registers[a.register])+'</span>';
			break;
		case 13:
			log += '<span class="command">OR</span>';
			var a = engine.numParse(engine.memory[engine.setCursor()]),
			    b = engine.numParse(engine.memory[engine.setCursor()]),
			    c = engine.numParse(engine.memory[engine.setCursor()]);
			if (a.type == 'register') {
				engine.registers[a.register] = (b.value | c.value);
				engine.drawRegisters();
			}
			log += ' '+a.str+' = '+b.str+' | '+c.str+' = '+engine.hexDecorate(engine.registers[a.register])+'<br /><span class="binary">'+engine.binStr(b.value)+'</span><br /><span class="binary" style="border-bottom:solid 1px #CCC;">'+engine.binStr(c.value)+'</span><br /><span class="binary">'+engine.binStr(engine.registers[a.register])+'</span>';
			break;
		case 14:
			log += '<span class="command">NOT</span>';
			var a = engine.numParse(engine.memory[engine.setCursor()]),
			    b = engine.numParse(engine.memory[engine.setCursor()]);
			if (a.type == 'register') {
				var value = ~b.value;
				value = value & 32767; // Flip that last bit back to zero; we're playing with 15-bit numbers, not 1's-compliment numbers
				engine.registers[a.register] = value;
				engine.drawRegisters();
			}
			log += ' '+a.str+' = ~'+b.str+' = '+engine.hexDecorate(engine.registers[a.register])+'<br /><span class="binary" style="border-bottom:solid 1px #CCC;">'+engine.binStr(b.value)+'</span><br /><span class="binary">'+engine.binStr(engine.registers[a.register])+'</span>';
			break;
		case 15:
			log += '<span class="command">RMEM</span>';
			var a = engine.numParse(engine.memory[engine.setCursor()]),
			    b = engine.numParse(engine.memory[engine.setCursor()]);
			if (a.type == 'register') {
				engine.registers[a.register] = engine.memory[b.value];
				engine.drawRegisters();
			}
			log += ' '+a.str+' = value at memory address '+b.str+' = '+engine.hexDecorate(engine.registers[a.register]);
			break;
		case 16:
			log += '<span class="command">WMEM</span>';
			var a = engine.numParse(engine.memory[engine.setCursor()]),
			    b = engine.numParse(engine.memory[engine.setCursor()]);
			engine.setMem(a.value, b.value);
			log += ' memory at '+a.str+' = '+b.str;
			break;
		case 17:
			log += '<span class="command">CALL</span>';
			var address = engine.numParse(engine.memory[engine.cursor]),
			    curCursor = engine.cursor+1;
			engine.stack.push(curCursor); // Save current place
			engine.setCursor(address.value); // Go to grabbed address
			log += ' '+address.str+', and pushing '+engine.hexDecorate(curCursor)+' to stack';
			break;
		case 18:
			log += '<span class="command">RET</span> ';
			engine.setCursor(engine.stack.pop());
			log += ' jumping back to '+engine.hexDecorate(engine.cursor);
			break;
		case 19:
			log += '<span class="command">OUT</span>';
			var ord = engine.numParse(engine.memory[engine.setCursor()]),
			    chr = String.fromCharCode(ord.value);
			log += ' '+ord.str+' = '+chr;
			$('#output .content').append(chr);
			break;
		case 20:
			if (engine.text_input == null || engine.text_input == '') {
				log += 'Pausing for text input.';
				engine.setCursor(engine.cursor-1);
				success = false;
				break;
			}
			log += '<span class="command">IN</span>';
			var a = engine.numParse(engine.memory[engine.setCursor()]),
			    chr = engine.text_input.substring(0,1);
			engine.text_input = engine.text_input.substring(1);
			if (a.type == 'register') {
				engine.registers[a.register] = chr.charCodeAt(0);
				$('#output .content').append(chr);
			}
			log += ' '+a.str+' = '+chr;
			break;
		case 21:
			log += '<span class="command">NOOP</span>';
			break;
		default:
			log += '<span class="command">UNKNOWN CMD '+cmd+'</span>';
			success = false;
	}
	if (logging == true) {
		var $console = $('#console');
		$console.find('.content').append('<p>'+log+'</p>');
		$console.stop(true).animate({scrollTop: $console.find('.content').height()}, 'fast');
	}
	return success;
}
