engine.run = function() {
	var ticks = 0,
	    start = +new Date(),
	    logging = $('select#logging').val();
	logging = (logging == 1)? true : false;
	while(true) {
		var rs = engine.do(logging);
		if (rs == false) break;
		ticks++;
		if (ticks >= 1000) break; // Pause every so often
		if (jQuery.inArray(engine.cursor, engine.breakpoints) != -1) break; // Stop at breakpoints
	}
	var delta = new Date()-start;
	console.log('ran '+ticks+' ticks in '+delta+' microseconds; '+ticks/(delta/1000)+' ops/sec');
}
$('button#run').click(engine.run);